package es.upm.fi.dia.oeg.obdi.wrapper.r2o.translator;

public class R2OTranslationException extends Exception {

	public R2OTranslationException(String string) {
		super(string);
	}

	public R2OTranslationException(Throwable arg0) {
		super(arg0);
	}

	public R2OTranslationException(String message, Throwable cause) {
		super(message, cause);
	}

	
}
